#include "minitalk.h"

int	ft_atoi(const char *s)
{
	unsigned int	n;
	int				i;
	int				sign;

	i = 0;
	n = 0;
	sign = 1;
	while ((s[i] == ' ' || s[i] == '\t' || s[i] == '\n')
		|| s[i] == '\f' || s[i] == '\v' || s[i] == '\r')
		i++;
	if (s[i] == '-')
		sign = -1;
	if (s[i] == '-' || s[i] == '+')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		n = n * 10 + (s[i] - '0');
		i++;
	}
	return ((int)(sign * n));
}

